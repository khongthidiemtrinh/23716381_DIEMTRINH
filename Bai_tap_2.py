print(" Các phép tính ")
a= float(input(" Nhập số nguyên a:"))
b= float(input(" Nhập số nguyên b:"))
print(" Tổng của hai số nguyên là: ", a+b)
print(" Hiệu của hai số nguyên là: ", a-b)
print(" Tích của hai số nguyên là: ", a*b)
print(" Thương của hai số nguyên là: ", round(a/b,2))
print(" Chia lấy dư của hai số nguyên là: ", a%b)
print(" Chia lấy nguyên của hai số nguyên là: ", a//b)

